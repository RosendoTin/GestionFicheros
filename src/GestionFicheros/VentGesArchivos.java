    private void btHaciaOrigenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btHaciaOrigenActionPerformed
        
        boolean correcto = true;
        
        if (dirOrigen == null || dirDestino == null)
        {
            correcto = false;
            JOptionPane.showMessageDialog(null,"Directorio de origen y destino debe de estar seleccionado");
        }     
        
        if (tbArcDestino.getSelectedRow() == -1 && correcto)
        {
            correcto = false;
            JOptionPane.showMessageDialog(null,"Seleccionar una fila");
        }  
        
        if (correcto)
        {
            ModTablaDestino modDesde = (ModTablaDestino) tbArcDestino.getModel();
            
            int filSeleccion = tbArcDestino.getSelectedRow();
            
            int filReal = tbArcDestino.convertRowIndexToModel(filSeleccion);
            
            File fichero = modDesde.movSeleccion(filReal);
            
            ModTablaArchivos modHacia = (ModTablaArchivos) tbARcOrigen.getModel();
            
            modHacia.agreFichero(fichero);
            
            modHacia.fireTableDataChanged();
            
            modDesde.fireTableDataChanged();
        }
        
    }//GEN-LAST:event_btHaciaOrigenActionPerformed

