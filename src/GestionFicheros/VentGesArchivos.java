    private void tfDirOrigenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tfDirOrigenMouseClicked
        dirOrigen = Utilidad.obtenerDirectorio();
        if (dirOrigen != null)
        {
            tfDirOrigen.setText(dirOrigen.getAbsolutePath());
            ModTablaArchivos modelo = (ModTablaArchivos) tbARcOrigen.getModel();
            
            modelo.recarTabla(Utilidad.obtFicheroDeDirectorio(dirOrigen));
            
            modelo.fireTableDataChanged();
           
        } else {
            tfDirOrigen.setText("");
        }
    }//GEN-LAST:event_tfDirOrigenMouseClicked

